    private void listarClientes() {
        DefaultTableModel tab = (DefaultTableModel) jTableClientes.getModel();
        
        tab.setNumRows(0);
        
        ArrayList<Cliente> clientes = clt.listarTodos();                
        
        for(Cliente cliente : clientes) {
            tab.addRow(new Object[] {cliente.getClienteId(), cliente.getNome(), cliente.getCpf(), cliente.getRg(), cliente.getDataNascimento(), 
                cliente.getSexo(), cliente.getEmail(), cliente.getTelefone(), cliente.getEndereco(), cliente.getBairro(), cliente.getCidade(), 
                cliente.getCep(), cliente.getCnh()});
        }
    }
    
