    private void btcadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcadActionPerformed
        //Cadastro de dados 
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy"); // Formato de data usual
                      
        cli.setNome(TxNomeCli.getText());                
        cli.setCpf(TxCpfCli.getText());
        cli.setRg(TxRgCli.getText());
        
          TxCpfCli.setText(" ");
        TxRgCli.setText(" ");
        
        try {
            // Converte para o tipo de Date (util) e retorna um long para o Date (SQL)
            cli.setDataNascimento(new java.sql.Date(df.parse(TxDnCli.getText()).getTime()));
        } catch (ParseException ex) {
            ex.printStackTrace(); // Mostra o erro detalhado

            System.out.println("Erro: " + ex.getMessage()); // Mostra o erro
        }
        TxDnCli.setText("  /  /     ");
        
        cli.setSexo(TxSexo.getText());
        cli.setEmail(TxEmailCli.getText());
        cli.setTelefone(TxTelCli.getText());
        cli.setEndereco(TxEndCli.getText());
        cli.setBairro(TxBairro.getText());
        cli.setCidade(TxCidade.getText());
        cli.setCep(TxCep.getText());
        cli.setCnh(TxCnhCli.getText());
                                        
        if(TxCod.getText().equals("")) {
            if(clt.cadastrar(cli))
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
        } else {
            cli.setClienteId(Integer.parseInt(TxCod.getText()));
            
            if(clt.alterar(cli))
                JOptionPane.showMessageDialog(null, "Cliente editado com sucesso");
        }
        
        listarClientes();
        TxNomeCli.setText("");
        TxCpfCli.setText("");
        TxRgCli.setText("");
        TxDnCli.setText("");
        TxSexo.setText("");
        TxEmailCli.setText("");
        TxEndCli.setText("");
        TxBairro.setText("");
        TxCidade.setText("");
        TxCep.setText("");
        TxTelCli.setText("");
        TxCnhCli.setText("");
        
        TxNomeCli.requestFocus();
        

    }//GEN-LAST:event_btcadActionPerformed
  
